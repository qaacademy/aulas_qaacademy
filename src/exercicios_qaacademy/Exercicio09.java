package exercicios_qaacademy;

public class Exercicio09 {
	
	/**
	 * Metodo para calcular a tabuada de um numero qualquer
	 * @param numeroDigitado 
	 */
	public void calcularTabuada(int numeroDigitado) {
			int i = 1;
			while (i <= 10) {
			System.out.println(numeroDigitado * i);
			i = i + 1;
		}
	}

}
